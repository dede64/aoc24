    func countMatchesWithRegex(in string: String) -> Int {
        let pattern = "XMAS"
        do {
            let regex = try NSRegularExpression(pattern: pattern)
            let matches = regex.matches(in: string, range: NSRange(string.startIndex..<string.endIndex, in: string))
            return matches.count
        } catch {
            return 0
        }
    }
    
    func rotate90Clockwise(_ grid: [[Character]]) -> [[Character]] {
        let rowCount = grid.count
        let colCount = grid[0].count
        var rotated = Array(repeating: Array(repeating: Character(" "), count: rowCount), count: colCount)
        
        for row in 0..<rowCount {
            for col in 0..<colCount {
                rotated[col][rowCount - row - 1] = grid[row][col]
            }
        }
        return rotated
    }
    
    func getDiagonal(_ grid: [[Character]]) -> [[Character]] {
        var result: [[Character]] = []
        let size = grid.count
        
        for row in 0..<size {
            var segment: [Character] = []
            for col in 0...row {
                segment.append(grid[row - col][col])
            }
            result.append(segment)
        }
        
        for row in 1..<size {
            var segment: [Character] = []
            for col in 0..<(size - row) {
                segment.append(grid[size - col - 1][row + col])
            }
            result.append(segment)
        }
        
        return result
    }
    
    // Function to flip the grid horizontally
    func flipHorizontally(_ grid: [[Character]]) -> [[Character]] {
        return grid.map { $0.reversed() }
    }

    // Helper function to print a grid
    func printGrid(_ grid: [[Character]]) {
        for row in grid {
            print(String(row))
        }
        print()
    }
    
    func solve() {
        let grid: [[Character]] = input.split(separator: "\n").map { Array($0) }
        print(grid.count, "x", grid[0].count)
        
        var rotations = [grid]
        rotations.append(rotate90Clockwise(rotations[0]))
        rotations.append(rotate90Clockwise(rotations[1]))
        rotations.append(rotate90Clockwise(rotations[2]))
        
        rotations.append(getDiagonal(rotations[0]))
        rotations.append(getDiagonal(rotations[1]))
        rotations.append(getDiagonal(rotations[2]))
        rotations.append(getDiagonal(rotations[3]))

        var result = 0
        for rotation in rotations {
            for line in rotation {
                let text = String(line)
                result += countMatchesWithRegex(in: text)
                
            }
        }
        print(result)
    }
    
